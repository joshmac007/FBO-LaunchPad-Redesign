// Global test utilities
global.mockApiResponse = (data, status = 200) => {
  return Promise.resolve({
    ok: status >= 200 && status < 300,
    status,
    json: () => Promise.resolve(data),
  })
}

global.mockApiError = (message, status = 500) => {
  return Promise.reject({
    status,
    message,
  })
} 